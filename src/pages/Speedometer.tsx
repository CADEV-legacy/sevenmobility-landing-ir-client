import * as S from './Speedometer.styles';

import { CONTROLLED_SECTIONS } from '@/3ds';
import { useIIIDMStore } from '@/stores';

export const Speedometer: React.FC = () => {
  const { section, sectionProgress } = useIIIDMStore();

  const sectionIndicator = useMemo(
    () => (section === 'detail' && sectionProgress === 100 ? 'Loaded' : 'Loading...'),
    [section, sectionProgress]
  );

  const sectionText = useMemo(() => {
    switch (section) {
      case 'loading':
        return 'Spec';
      case 'spec':
        return 'Battery';
      case 'battery':
        return 'BMS';
      case 'bms':
        return 'MCU';
      case 'mcu':
        return 'Motor';
      case 'electricMotor':
        return 'R · B';
      case 'regenerativeBraking':
        return 'U · R';
      case 'userReview':
        return 'Detail';
      case 'detail':
        return 'Detail';
      default:
        return '';
    }
  }, [section]);

  const percentage = useMemo(() => {
    switch (section) {
      case 'loading':
        return sectionProgress / CONTROLLED_SECTIONS.length;
      case 'spec':
        return (100 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'battery':
        return (200 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'bms':
        return (300 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'mcu':
        return (400 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'electricMotor':
        return (500 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'regenerativeBraking':
        return (600 + sectionProgress) / CONTROLLED_SECTIONS.length;
      case 'userReview':
      case 'detail':
        return (700 + sectionProgress) / CONTROLLED_SECTIONS.length;
      default:
        return 0;
    }
  }, [section, sectionProgress]);

  return (
    <S.SpeedometerContainer>
      <SpeedometerSVG percentage={percentage} />
      <S.SpeedometerContent>
        <Typography variant='h4' textAlign='center' sx={{ marginBottom: '.25rem' }}>
          {sectionIndicator}
        </Typography>
        <Typography
          variant='h2'
          textAlign='center'
          fontWeight='bold'
          sx={{ marginBottom: '.5rem' }}>
          {sectionText}
        </Typography>
        <Typography variant='h3' textAlign='center' fontWeight='bold'>
          {sectionProgress.toFixed(0)} %
        </Typography>
      </S.SpeedometerContent>
    </S.SpeedometerContainer>
  );
};

